fun main() {
    var a: Int = 23
    var f: Float = 5.75F
    var c: Char = 'T'
    var s: String = "Tanay"
    var b: Boolean = true
    var d: Double = 2.9
    var l: Long = 9999
    var h: Short = 230
    var t: Byte = 127

    println("Integer value: $a")
    println("Float value: $f")
    println("Character value: $c")
    println("String value: $s")
    println("Boolean value:$b")
    println("Double value:$d")
    println("Long value:$l")
    println("Short value:$h")
    println("Byte value:$t")
}